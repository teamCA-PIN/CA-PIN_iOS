import Foundation
import UIKit

extension UIFont {
  class func notoSansKRMediumFont (fontSize: CGFloat) -> UIFont {
    return UIFont(name: "NotoSansKR-Medium", size: fontSize)!
  }
  class func notoSansKRBlackFont (fontSize: CGFloat) -> UIFont {
    return UIFont(name: "NotoSansKR-Black", size: fontSize)!
  }
  class func notoSansKRBoldFont (fontSize: CGFloat) -> UIFont {
    return UIFont(name: "NotoSansKR-Bold", size: fontSize)!
  }
  class func notoSansKRLightFont (fontSize: CGFloat) -> UIFont {
    return UIFont(name: "NotoSansKR-Light", size: fontSize)!
  }
  class func notoSansKRRegularFont (fontSize: CGFloat) -> UIFont {
    return UIFont(name: "NotoSansKR-Regular", size: fontSize)!
  }
  class func notoSansKRThinFont (fontSize: CGFloat) -> UIFont {
    return UIFont(name: "NotoSansKR-Thin", size: fontSize)!
  }
}
