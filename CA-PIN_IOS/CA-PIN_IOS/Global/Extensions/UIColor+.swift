import UIKit

extension UIColor {    
  @nonobjc class var primaryWhite: UIColor {
    return UIColor(white: 1.0, alpha: 1.0)
  }
  @nonobjc class var primaryBlack: UIColor {
    return UIColor(white: 8.0 / 255.0, alpha: 1.0)
  }
  @nonobjc class var primaryGray: UIColor {
    return UIColor(red: 245.0 / 255.0,
                   green: 246.0 / 255.0,
                   blue: 250.0 / 255.0,
                   alpha: 1.0)
  }
  
  
}
